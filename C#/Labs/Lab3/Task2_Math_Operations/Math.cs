using System;
using System.Collections.Generic;
using System.Text;

namespace Task2_Math_Operations
{
    static class Math
    {
        public static decimal add(int left, decimal right)
        {
            return left + right;
        }
        public static decimal sub(int left, decimal right)
        {
            return left - right;
        }
        public static decimal mult(int left, decimal right)
        {
            return left * right;
        }
        public static decimal div(decimal left, decimal right)
        {
            return left / right;
        }

        
    }
}
